public class Sintaxe01 {

// Explicação detalhada da sintaxe utilizada no Exercício 1 – Identificar mundos

// 1. public
//    Indica que a classe ou método pode ser acessado por qualquer outra classe.
//    É como se disséssemos: "Qualquer programa ou pessoa pode ver este café / pedido".

// 2. class
//    Define uma classe em Java. Uma classe é como um molde ou receita para criar objetos.
//    No nosso caso, a "classe" será o recipiente que guarda todo o exercício.

// 3. Nome da classe
//    Existe um CONVENÇÃO: sempre começa com letra maiúscula e deve ter significado.
//    Ex.: IdentificarMundos → indica que esta classe lida com o Exercício 1.

// 4. Conteúdo da classe
//    Tudo que pertence à classe vai dentro das chaves { }.
//    Inclui métodos, variáveis, comentários e qualquer lógica necessária.

// 5. public static void main(String[] args)
//    Este método é o ponto de entrada do programa, onde ele começa a executar.
//    - public → acessível a qualquer parte do programa
//    - static → não precisa criar um objeto da classe para usar
//    - void → não retorna nada
//    - main(String[] args) → permite receber informações externas se necessário
//    Analogia ao café: é como o barista que inicia o preparo de todos os pedidos, sem precisar perguntar a ninguém antes.

// 6. System.out.println("")
//    Comando usado para imprimir mensagens no console.Quebra de linha (\n):
//       - \n é um caractere especial que indica "nova linha".
//       - Ex.: System.out.println("Linha 1\nLinha 2"); 
//         Resultado no console:
//         Linha 1
//         Linha 2
//    Tudo que colocamos dentro das aspas será mostrado na tela.
//    Analogias:
//       - Mundo real: falar para o cliente o que ele vai receber.
//       - Matrix: registrar informações no sistema digital para as pessaos visualizarem.

// OBS: A ideia é que, após entender este arquivo, qualquer Exercício.java
// pode ser escrito usando apenas estes conceitos, sem surpresas.

}
